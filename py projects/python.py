
firstNumber = float(input("Enter a First Number : "))
secondNumber = float(input("Enter a Second Number : "))

sum = firstNumber + secondNumber

print("the sum of ", str(firstNumber) + " + " + str(secondNumber) + " is equal to " + str(sum))

difference = firstNumber - secondNumber

print("the difference of ", str(firstNumber) + " - " + str(secondNumber) + " is equal to " + str(difference))

product = firstNumber * secondNumber

print("the product of ", str(firstNumber) + " * " + str(secondNumber) + " is equal to " + str(product))

quotient = firstNumber / secondNumber

print("the quotient of ", str(firstNumber) + " / " + str(secondNumber) + " is equal to " + str(quotient))